# Classification template

# Importing the libraries
import numpy as np
import matplotlib.pyplot as plt
import pandas as pd

# Importing the dataset
dataset = pd.read_csv('Social_Network_Ads.csv')
X = dataset.iloc[:, [2, 3]].values
y = dataset.iloc[:, 4].values

# Splitting the dataset into the Training set and Test set
from sklearn.model_selection import train_test_split
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size = 0.25, random_state = 0)

# Feature Scaling
from sklearn.preprocessing import StandardScaler
sc = StandardScaler()
X_train = sc.fit_transform(X_train)
X_test = sc.transform(X_test)

# Fitting classifier to the Training set
from sklearn.svm import SVC
classifier  = SVC(kernel = "rbf", random_state = 0)
classifier.fit(X_train , y_train)


#here we use the k-fold cross validation for better evaluation
from sklearn.model_selection import cross_val_score
#it will return 10 accuracies to evaluate the 10 folds of acccuracy
accuracies = cross_val_score(estimator = classifier ,X = X_train , y = y_train , cv = 10 )
accuracies.mean() #it is the relevant information of our model evaluation
# we can also compute standard deviation to calculate how much it varies
accuracies.std()

#applying grid search to find the best parameters to find best model and parametres
from sklearn.model_selection import GridSearchCV
paramters = [{"C" : [1,10,100,1000] , "kernel" : ["linear"]},
              {"C" : [1, 10 ,100,1000], "kernel" : ["rbf"], "gamma": [ .5 , .1 , .01 , .001 , .0001]} 
              ]
grid_search = GridSearchCV(estimator = classifier, param_grid = paramters , scoring = "accuracy" , cv = 10 , n_jobs = -1) #cv = 10 mean 10 fold cross validation will be performed across grid search
grid_search = grid_search.fit(X_train,y_train)
best_accuracy = grid_search.best_score_

# Predicting the Test set results
y_pred = classifier.predict(X_test)

# Making the Confusion Matrix
from sklearn.metrics import confusion_matrix
cm = confusion_matrix(y_test, y_pred)

# Visualising the Training set results
from matplotlib.colors import ListedColormap
X_set, y_set = X_train, y_train
X1, X2 = np.meshgrid(np.arange(start = X_set[:, 0].min() - 1, stop = X_set[:, 0].max() + 1, step = 0.01),
                     np.arange(start = X_set[:, 1].min() - 1, stop = X_set[:, 1].max() + 1, step = 0.01))
plt.contourf(X1, X2, classifier.predict(np.array([X1.ravel(), X2.ravel()]).T).reshape(X1.shape),
             alpha = 0.75, cmap = ListedColormap(('red', 'green')))
plt.xlim(X1.min(), X1.max())
plt.ylim(X2.min(), X2.max())
for i, j in enumerate(np.unique(y_set)):
    plt.scatter(X_set[y_set == j, 0], X_set[y_set == j, 1],
                c = ListedColormap(('red', 'green'))(i), label = j)
plt.title('SVM (Training set)')
plt.xlabel('Age')
plt.ylabel('Estimated Salary')
plt.legend()
plt.show()

